    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       String tenDangNhap = jTextField1.getText().trim();
    String matKhau = jPasswordField1.getText().trim();
    //kiểm tra tên đăng nhập hoặc mật khẩu rỗng 
    if (tenDangNhap.isEmpty() || matKhau.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ tài khoản và mật khẩu.");
        return;
    }

    Connection conn = null;
    PreparedStatement stmt = null;

    try {
        DB db = new DB();             
        conn = db.con();             

        String sql = "INSERT INTO dang_nhap (ten_dang_nhap, mat_khau) VALUES (?, ?)";
        stmt = conn.prepareStatement(sql);
        stmt.setString(1, tenDangNhap);
        stmt.setString(2, matKhau);
        stmt.executeUpdate();

        JOptionPane.showMessageDialog(this, "Đăng ký thành công!");
        
        this.dispose();                       
        new LoginForm().setVisible(true);     

    } catch (SQLException ex) {
    if (ex.getErrorCode() == 1062) {
        //cái này 1062 là lỗi khi insert hoặc cập nhập giá trị đã tồn tại trong bảng
        JOptionPane.showMessageDialog(this, "Tên đăng nhập đã tồn tại!");
    } else {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(this, "Lỗi SQL: " + ex.getMessage());
    }
    }
    }//GEN-LAST:event_jButton2ActionPerformed

    
